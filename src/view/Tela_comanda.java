    private void table_consumoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_consumoMouseClicked
        Point p = evt.getPoint();
        int table_row = table_consumo.getSelectedRow();
        boolean para = false;
       
        if (evt.getClickCount() == 2) {
            for(Consumo c :lista_consumo){
                String hora = new SimpleDateFormat("HH:mm").format(c.getHr_consumo());
                for(Produto produtos : lista_produtos){
                   if((table_consumo.getValueAt(table_row, 0)==produtos.getNome())&&(produtos.getId_produto()==c.getIdproduto())
                           &&(hora.equals(table_consumo.getValueAt(table_row, 2)))) {
                       String qtd = (String) table_consumo.getValueAt(table_row, 1);
                       Object quantidade;
                       if(verificaKilo(c)){
                           qtd = qtd.substring(0,qtd.length()-2);
                       }
                       if(c.getQuantidade()== Float.valueOf(qtd)){
                           Object[] options = {"Sim","Não"};
                           int resposta = JOptionPane.showOptionDialog(rootPane, "Deseja excluir o seguinte item? \n"+produtos.getNome()
                                   + "\nPreço:"+String.format("%.2f",produtos.getPreco())
                                   + "\nQuantidade: "+c.getQuantidade(), "Apagar item", 0, 3, null, options, options[0]);
                           if(resposta==0){        
                                try{
                                   ComandaDAO comandaDAO = new ComandaDAO();
                                   transfer_data.getComanda().setTotal(transfer_data.getComanda().getTotal()- (produtos.getPreco()*c.getQuantidade()));
                                   comandaDAO.alterar_total(transfer_data.getComanda().getTotal(), transfer_data.getComanda().getId());
                                   ConsumoDAO consumoDAO = new ConsumoDAO(); 
                                   consumoDAO.remover_consumo(c.getIdconsumo());
                                   refreshTable();
                                   float soma = (transfer_data.getComanda().getTotal());                     
                                   label_total.setText("R$ "+ String.format("%.2f",soma));
                                   para=true;
                                } catch (SQLException ex) {
                                 Logger.getLogger(Tela_login.class.getName()).log(Level.SEVERE, null, ex);
                                   }
                           }
                       }
                       
                   }
                   if(para)break;
                }
                if(para)break;
            }
        }
    }//GEN-LAST:event_table_consumoMouseClicked

