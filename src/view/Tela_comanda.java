    public void add_Produto(String codigo){
        DAO dao;
        DAO daoComanda;
        float peso = 0.3f;
        boolean valido = false;
        try {
            dao = new ConsumoDAO();
            Consumo consumo = new Consumo();
            daoComanda = new ComandaDAO();
            
            for (Produto p : transfer_data.getLista_produtos())
            {               
               if (p.getBar_code().equals(codigo)){
                   if(p.getPorkilo()==1){
                      peso = porKilo();
                      consumo.setQuantidade(peso);
                   }else{
                       consumo.setQuantidade(Float.valueOf((String)combobox_qtd.getSelectedItem()));
                   }
                   consumo.setIdcomanda( transfer_data.getComanda().getId());
                   consumo.setIdproduto(p.getId_produto());                   
                   dao.salvar(consumo);
                   refreshTable();
                   valido = true;
                   textfield_produto.setText("");
                   combobox_qtd.setSelectedItem("1");
                   transfer_data.getComanda().setTotal(transfer_data.getComanda().getTotal()+(consumo.getQuantidade()*p.getPreco()));
                   
                   daoComanda.alterar_total(transfer_data.getComanda().getTotal(), transfer_data.getComanda().getId());
                   break;
               }               
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(Tela_login.class.getName()).log(Level.SEVERE, null, ex);
        }catch (NumberFormatException ex) {
            valido= false;
        }
        
        for(Usuario user: transfer_data.getLista_usuarios()){
            if (user.getCodigo().equals(textfield_produto.getText())){
                valido=true;
                this.dispose();
                transfer_data.getTela_login().bloqueia_tela();
            }
        }
        if(!valido){
            JOptionPane.showMessageDialog(rootPane, "Código inválido!","ERRO",
                     JOptionPane.ERROR_MESSAGE);
            textfield_produto.setText("");
        }
        
    }
