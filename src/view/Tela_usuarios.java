    private void table_usuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_usuariosMouseClicked
       
        Usuario usuario = null;
        if (evt.getClickCount() == 2) {
            int row= table_usuarios.getSelectedRow();
            for(Usuario user : lista_usuarios){
                if (user.getCodigo().equals(table_usuarios.getValueAt(row, 1))){
                    Object[] options = {"Sim","Não"};
                                   int resposta = JOptionPane.showOptionDialog(rootPane, "Deseja excluir o seguinte usuário? \nNome: "+user.getNome()
                                           + "\nCódigo: "+user.getCodigo()
                                           + "\nFunção: "+table_usuarios.getValueAt(row, 2), "Apagar item", 0, 3, null, options, options[0]);
                    if(resposta==0){  
                       
                        try {
                            UsuarioDAO usuarioDAO = new UsuarioDAO();
                            usuarioDAO.remover_usuario(user.getCodigo()); 
                            usuario = user;
                            data_transfer.getLista_usuarios().remove(usuario);
                            
                        } catch (SQLException ex) {
                            Logger.getLogger(Tela_usuarios.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } 
                }
            }
            if(usuario!=null)refreshLista(usuario);
        }
    }//GEN-LAST:event_table_usuariosMouseClicked

    public void refreshLista(Usuario user){
        lista_usuarios.remove(user);
        refreshTable();
    }
