    public void refreshTable (){
        
        int row_counter =0;
            
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) table_usuarios.getModel();
            model.setRowCount(lista_usuarios.size()); 
            for (Usuario u : lista_usuarios){ 
              table_usuarios.setValueAt(u.getNome(), row_counter, 0);   
              table_usuarios.setValueAt(u.getCodigo(), row_counter, 1);  
              if(u.getGerente()==1){
                table_usuarios.setValueAt("Gerente", row_counter, 2);  
              }else{
                table_usuarios.setValueAt("Atendente", row_counter, 2);   
              }
              
              row_counter++; 
            }
        
    }
    
    public void addUsuario (){
        
        try{
            int codigo = Integer.valueOf(text_codigo.getText());        
        
        if(verificaUsuarios(text_codigo.getText())){
        
            DAO dao;  
            Usuario usuario = new Usuario();
            usuario.setCodigo(text_codigo.getText());
            usuario.setNome(text_nome.getText());
            if(radio_gerente.isSelected()){
                usuario.setGerente(1);
            }else{
                usuario.setGerente(0);
            }
            try {
                dao = new UsuarioDAO();
                dao.salvar(usuario);
                lista_usuarios.add(usuario);
                refreshTable();
                text_codigo.setText("");
                text_nome.setText("");
                text_nome.requestFocus();
                data_transfer.getLista_usuarios().add(usuario);

            } catch (SQLException ex) {
                Logger.getLogger(Tela_login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Código ja utilizado por outro usuário!","ERRO",
                         JOptionPane.ERROR_MESSAGE);
            text_codigo.setText("");
            text_codigo.requestFocus();
        }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(rootPane, "Código inválido, digite apenas números!","ERRO",
                         JOptionPane.ERROR_MESSAGE);
            text_codigo.setText("");
            text_codigo.requestFocus();
        }
    }
    
    public boolean verificaUsuarios(String codigo){
        for(Usuario user : lista_usuarios){
            if(codigo.equals(user.getCodigo()))return false;
        }
        return true;
    }
    
    public void bloqueia_tela(){
         boolean autorizado = false;     
            
            lista_usuarios = new ArrayList<Usuario>();
            DAO dao;
            while (autorizado==false){
                String cod_atendente= JOptionPane.showInputDialog(rootPane, "Gerente:", "Login", JOptionPane.QUESTION_MESSAGE);
                try {
                    dao = new UsuarioDAO();
                    lista_usuarios = dao.listarTodos();

                    for (Usuario u : lista_usuarios)
                    {
                       if ((u.getCodigo().equals(cod_atendente))&&(u.getGerente()==1)){
                           autorizado = true;
                           break;
                       }

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Tela_login.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (autorizado== true){  
                    text_nome.requestFocus();
                    refreshTable();

                }else{
                    JOptionPane.showMessageDialog(rootPane, "Acesso Não Autorizado!","ERRO",
                     JOptionPane.ERROR_MESSAGE);
            }
            }
        
    }
